package extendssample;

public class Mario {
  //歩く
  public void walk(){
    System.out.println("Mario.walkです。歩きます");
  }
  //走る
  public void ran(){
    System.out.println("Mario.runです。走ります");
  }
  //ジャンプします
  public void jump(){
    System.out.println("Mario.runです。走ります。");
  }
  //ふみます
  public void tread(){
    System.out.println("Mario.treadです。踏みます。");
  }

}
